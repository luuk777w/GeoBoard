export class UserViewModel {
    /**
     * The unique identifier of this user.
     */
    id: string;

    /**
     * The unique name of this user.
     */
    userName: string;

    /**
     * The user's email address used to login.
     */
    email: string;

    /**
     * The date and time of when this user was created.
     */
    createdAt: Date;
}
